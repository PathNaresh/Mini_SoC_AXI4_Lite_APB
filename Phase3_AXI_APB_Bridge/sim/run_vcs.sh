
#!/bin/bash

#=========================================================
# AXI -> APB BRIDGE SIMULATION SCRIPT
#=========================================================

echo ""
echo "=========================================="
echo "  AXI -> APB BRIDGE SIMULATION START"
echo "=========================================="
echo ""

#=========================================================
# LOAD VCS
#=========================================================
module load vcs/O-2018.09-SP2

#=========================================================
# CLEAN OLD FILES
#=========================================================
rm -rf csrc
rm -rf simv*
rm -rf ucli.key
rm -rf *.vpd
rm -rf DVEfiles

#=========================================================
# COMPILE
#=========================================================
vcs -sverilog \
-full64 \
-debug_access+all \
+incdir+../../common \
../rtl/axi_apb_bridge.sv \
../rtl/apb_gpio.sv \
../tb/tb_axi_apb_bridge.sv \
-l compile.log

#=========================================================
# RUN
#=========================================================
./simv -l sim.log

echo ""
echo "=========================================="
echo "  SIMULATION COMPLETED"
echo "=========================================="
echo ""

