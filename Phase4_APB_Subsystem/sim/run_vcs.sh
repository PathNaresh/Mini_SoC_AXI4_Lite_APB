
#!/bin/bash

#=========================================================
# APB SUBSYSTEM SIMULATION SCRIPT
#=========================================================

echo ""
echo "=========================================="
echo "     APB SUBSYSTEM SIMULATION START"
echo "=========================================="
echo ""

#=========================================================
# LOAD VCS
#=========================================================
module load vcs/O-2018.09-SP2

#=========================================================
# CLEAN OLD FILES
#=========================================================
rm -rf csrc
rm -rf simv*
rm -rf ucli.key
rm -rf *.vpd
rm -rf DVEfiles

#=========================================================
# COMPILE
#=========================================================
vcs -sverilog \
-full64 \
-debug_access+all \
+incdir+../../common \
../rtl/apb_decoder.sv \
../rtl/apb_gpio.sv \
../rtl/apb_uart_stub.sv \
../rtl/apb_spi_stub.sv \
../rtl/apb_subsystem.sv \
../tb/tb_apb_subsystem.sv \
-l compile.log

#=========================================================
# RUN
#=========================================================
./simv -l sim.log

echo ""
echo "=========================================="
echo "     SIMULATION COMPLETED"
echo "=========================================="
echo ""

